
import random

skip = 4

ts = [
    (10,10),
    (10,10),
    (100,100),
    (100,100),
    (100,100),
    (100,100),
    (500,500),
    (500,500),
    (1000,1000),
    (2000,1000),
    (3000,1000),
    (10000,1000),
    (10000,1000),
    (10000,1000),
    (100000,1000),
    (100000,1000),
    (100000,1000),
    (100000,1000),
    (100000,1000),
]

for i, (n,m) in enumerate(ts):

    a = random.randint(0, n)
    b = random.randint(1, m)

    with open('%02d.in' % (i + skip), 'w') as f:
        f.write('%d %d\n' % (a,b))

